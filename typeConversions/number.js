// Number() is used to convert other dataypes to equivalent number
// parseInt(), parseFloat() are also best alternatives of Number()


console.log(Number("16.1818")); // 16.1818
console.log(Number(false)); // 0
console.log(Number(true)); // 1
console.log(Number("")); // 0
console.log(Number("Hai")); // NaN
console.log(Number(" ")); // NOTE, spaces are also considered as 0
