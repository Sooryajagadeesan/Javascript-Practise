// String() is used to convert other dataypes to equivalent string
// String() converts the passed arguments with same content to a string.
// toString() is an alternative of String()



console.log(String(18), "of TYPE", typeof String(18)); // '18'
console.log(String(false), "of TYPE", typeof String(false)); // "false"
console.log(String(true), "of TYPE", typeof String(true)); // "true"
console.log(String(null), "of TYPE", typeof String(null)) // "null"
console.log(String(undefined), "of TYPE", typeof String(undefined)) // "undefined"
