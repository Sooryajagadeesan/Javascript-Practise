// charAt(index) -> returns the character at the specified index.

let proverb = "Well Done is Better than Well Said";

console.log(`Character at index 1 is '${proverb.charAt(1)}'`); // e
console.log(`Character at index 0 is '${proverb.charAt(0)}'`); // W
console.log(`Character at index 3 is '${proverb.charAt(3)}'`); // l
