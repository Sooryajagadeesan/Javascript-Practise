// str1.includes(str2) -> returns true if str2 is in str1 else returns false

let proverb = "Be Positive !";

console.log(proverb.includes('o')); // true
console.log(proverb.includes(' ')); // true
console.log(proverb.includes('B')); // true
console.log(proverb.includes('!')); // true
console.log(proverb.includes('b')); // false, beacause of case sensitive behaviour