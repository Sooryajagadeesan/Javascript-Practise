// substr(startIndex,countOfCharacters)
// returns a sub string starting from the startIndex and takes size of countOfCharacters

let proverb = "You can and so You Should make it POSSIBLE!!";

console.log(proverb.substr(0,5)); //'You c'
console.log(proverb.substr(0,10)); //'You can an'
console.log(proverb.substr(4,10)); // 'can and so'
