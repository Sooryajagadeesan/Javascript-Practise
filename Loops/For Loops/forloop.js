//  for loop is used to iterate a block of statement for 'n' number of times

// simple for loop, to log first 10 natural numbers

console.log("First 10 natural numbers");
for(let i = 1; i<=10;i++) {
    console.log(`Natural number ${i}: ${i}`);
}

// 11 table for first 5 multiples
console.log("\nFirst 5 multiples of 11");
for(let i = 1; i<=5;i++){
    console.log(`11 x ${i} = ${11*i}`);
}