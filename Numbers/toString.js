// number.toString(base) is used to convert a number to a string based on the base parameter, base is 10 (for decimal) by default, 

let number = 15;

// number as a string
console.log("Number as a string : "+number.toString());

// number as a binary string
console.log("Number String in Binary form : "+ number.toString(2));

// number as a octal string
console.log("Number String in Octal form : "+ number.toString(8));

// number as a hexadecimal string
console.log("Number String in Hexadecimal form : "+ number.toString(16));