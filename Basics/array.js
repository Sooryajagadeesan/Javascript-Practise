// Arrays are used to store sequence of data, the data can be of different types

// basic array of numbers
let numbers = [1,2,3,4,5,6];

// basic array of strings
let words = ["HTML", 'CSS', `JS`];

// array containing all the data types
let mySampleArray = [18, 2.01, 'Developer', true, `Hard Working`, undefined,null];

/*null and undefined are among the 8 data types in Javascript, so they can also be assigned to variables*/

//  logging the values to the console.
console.log('Simple Numbers Array:', numbers); // by default the comma seperator adds a space between the arguments
console.log(`Simple String Array:`, words);
console.log('Arrays of all types:',mySampleArray);


