//  Accessing array elements

// We can use index values to access them 
// index values start from 0 and go till (length -1)


let webLanguages = ["HTML","CSS","Javascript","PHP","Ruby","GO","Python","Java"];
let size = webLanguages.length // length is a property that gets the length of array
console.log('First Element :',webLanguages[0]);
console.log('Second Element :',webLanguages[1]);
console.log('Last Element :',webLanguages[size-1]);